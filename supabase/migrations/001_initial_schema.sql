  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_agent_analyses_type ON agent_analyses(analysis_type, created_at DESC);
CREATE INDEX idx_agent_analyses_severity ON agent_analyses(severity, created_at DESC);
CREATE INDEX idx_agent_analyses_market ON agent_analyses(source_market, created_at DESC);

COMMENT ON TABLE agent_analyses IS 'AI-generated intelligence insights from the monitoring agent';

-- ================================================================
-- TABLE 3: arrival_stats
-- Verified arrival statistics (official government data only)
-- ================================================================
CREATE TABLE arrival_stats (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  recorded_at TIMESTAMPTZ DEFAULT NOW(),

  -- Period classification
  period_type VARCHAR(20) NOT NULL, -- 'annual', 'monthly', 'h1', 'q1', etc.
  period_label VARCHAR(50) NOT NULL, -- '2025', 'Jan 2025', 'H1 2025'
  year INTEGER NOT NULL,
  month INTEGER, -- null for annual/half-year/quarter

  -- Tourist counts
  domestic_tourists BIGINT,
  foreign_tourists BIGINT,
  total_tourists BIGINT,

  -- Charter operations
  charter_flights INTEGER,
  charter_tourists INTEGER,

  -- Scheduled international operations
  scheduled_intl_flights INTEGER,
  scheduled_intl_tourists INTEGER,

  -- Cruise operations
  cruise_vessels INTEGER,
  cruise_passengers_foreign INTEGER,
  cruise_passengers_domestic INTEGER,

  -- Data provenance
  data_source VARCHAR(200) NOT NULL, -- URL or reference
  data_release_date DATE, -- when govt published this
  is_provisional BOOLEAN DEFAULT false,

  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_arrival_stats_period ON arrival_stats(year, month);
CREATE INDEX idx_arrival_stats_type ON arrival_stats(period_type);

COMMENT ON TABLE arrival_stats IS 'Official government tourism arrival statistics for Goa';

