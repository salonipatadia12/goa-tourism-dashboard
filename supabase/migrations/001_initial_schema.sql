-- Latest flight prices by route
CREATE VIEW v_latest_flight_prices AS
SELECT DISTINCT ON (origin_iata, destination_iata)
  origin_iata,
  origin_city,
  origin_country,
  destination_iata,
  destination_name,
  price_amount,
  price_currency,
  price_inr,
  airline,
  direct_flight,
  fetched_at
FROM flight_prices
ORDER BY origin_iata, destination_iata, fetched_at DESC;

-- Recovery progress vs 2017 benchmark
CREATE VIEW v_recovery_progress AS
SELECT
  period_label,
  year,
  month,
  foreign_tourists,
  domestic_tourists,
  total_tourists,
  charter_tourists,
  scheduled_intl_tourists,
  CASE
    WHEN year = 2017 THEN foreign_tourists
    ELSE NULL
  END as benchmark_2017,
  CASE
    WHEN year > 2017 THEN
      ROUND((foreign_tourists::DECIMAL /
        (SELECT foreign_tourists FROM arrival_stats WHERE year = 2017 AND period_type = 'annual')::DECIMAL) * 100, 2)
    ELSE NULL
  END as recovery_percentage
FROM arrival_stats
WHERE period_type IN ('annual', 'monthly')
ORDER BY year DESC, month DESC;

-- Latest analyses by severity
CREATE VIEW v_latest_analyses AS
SELECT
  analysis_type,
  title,
  summary,
  severity,
  source_market,
  confidence,
  actionable,
  recommended_action,
  created_at
FROM agent_analyses
WHERE expires_at IS NULL OR expires_at > NOW()
ORDER BY
  CASE severity
    WHEN 'critical' THEN 1
    WHEN 'warning' THEN 2
    WHEN 'positive' THEN 3
    WHEN 'info' THEN 4
  END,
  created_at DESC;

