-- Enable UUID extension
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ================================================================
-- TABLE 1: flight_prices
-- Real-time flight prices (populated by Amadeus API agent)
-- ================================================================
CREATE TABLE flight_prices (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  fetched_at TIMESTAMPTZ DEFAULT NOW(),

  -- Origin details
  origin_iata VARCHAR(10) NOT NULL,
  origin_city VARCHAR(100) NOT NULL,
  origin_country VARCHAR(100) NOT NULL,

  -- Destination details
  destination_iata VARCHAR(10) NOT NULL,
  destination_name VARCHAR(100) NOT NULL,

  -- Flight details
  departure_date DATE NOT NULL,
  return_date DATE,

  -- Pricing
  price_currency VARCHAR(3) DEFAULT 'EUR',
  price_amount DECIMAL(10,2),
  price_inr DECIMAL(10,2),

  -- Flight characteristics
  airline VARCHAR(100),
  direct_flight BOOLEAN DEFAULT false,
  cabin_class VARCHAR(20) DEFAULT 'ECONOMY',

  -- Metadata
  data_source VARCHAR(50) DEFAULT 'amadeus',
  raw_response JSONB,

  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Indexes for efficient querying
CREATE INDEX idx_flight_prices_route ON flight_prices(origin_iata, destination_iata, fetched_at DESC);
CREATE INDEX idx_flight_prices_date ON flight_prices(departure_date, fetched_at DESC);
CREATE INDEX idx_flight_prices_destination ON flight_prices(destination_iata, fetched_at DESC);

COMMENT ON TABLE flight_prices IS 'Real-time flight price data from Amadeus API for Goa vs competitor destinations';

-- ================================================================
-- TABLE 2: agent_analyses
-- Intelligence insights generated by the agent
-- ================================================================
CREATE TABLE agent_analyses (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  created_at TIMESTAMPTZ DEFAULT NOW(),

  -- Analysis metadata
  analysis_type VARCHAR(50) NOT NULL,
  -- Types: 'price_comparison', 'demand_signal', 'campaign_recommendation',
  --        'charter_opportunity', 'competitor_alert', 'market_trend'

  -- Content
  title VARCHAR(200) NOT NULL,
  summary TEXT NOT NULL,
  details JSONB NOT NULL,

  -- Classification
  severity VARCHAR(20) DEFAULT 'info', -- 'info', 'warning', 'critical', 'positive'
  source_market VARCHAR(100),

  -- Quality metrics
